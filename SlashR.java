class SlashR{
    public static void main(String[] args){
        System.out.println("Prince\r");
        System.out.println("Princ\re");
        System.out.println("Prin\rce");
        System.out.println("Pri\rnce");
        System.out.println("Pr\rince");
        System.out.println("P\rrince");
        System.out.println("\rPrince");
    }
}